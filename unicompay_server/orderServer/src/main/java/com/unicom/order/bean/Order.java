package com.unicom.order.bean;

import com.tydic.common.bean.BaseModel;

import java.util.Date;

/**
 * Created by sh-zhaogx3 on 2014/7/31.
 */
public class Order extends BaseModel {
    private String id;
    private String phoneNumber;
    private String imsi;
    private String status;
    //订购时间
    private Date effectDate;
    //创建时间
    private Date createTime;
    //退订时间
    private Date cancelDate;

    private String prevOrderId;

    private String context;

    private String serviceId;


    public Date getEffectDate() {
        return effectDate;
    }

    public void setEffectDate(Date effectDate) {
        this.effectDate = effectDate;
    }

    public String getPhoneNumber() {
        return phoneNumber;
    }

    public void setPhoneNumber(String phoneNumber) {
        this.phoneNumber = phoneNumber;
    }

    public Date getCreateTime() {
        return createTime;
    }

    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }

    public Date getCancelDate() {
        return cancelDate;
    }

    public void setCancelDate(Date cancelDate) {
        this.cancelDate = cancelDate;
    }

    public String getImsi() {
        return imsi;
    }

    public void setImsi(String imsi) {
        this.imsi = imsi;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getId() {
        return id;
    }

    public String getContext() {
        return context;
    }

    public void setContext(String context) {
        this.context = context;
    }

    public String getPrevOrderId() {
        return prevOrderId;
    }

    public void setPrevOrderId(String prevOrderId) {
        this.prevOrderId = prevOrderId;
    }

    public String getServiceId() {
        return serviceId;
    }

    public void setServiceId(String serviceId) {
        this.serviceId = serviceId;
    }
}
